package newYearGift;

public class Chocolates {
	 int chocoWeight;
	 int chocoPrice;
	//constructor
	
	public Chocolates(int chocoWeight, int chocoPrice) {
		super();
		this.chocoWeight = chocoWeight;
		this.chocoPrice = chocoPrice;
	}
	
	public int getChocoWeight() {
		return chocoWeight;
	}


	public void setChocoWeight(int chocoWeight) {
		this.chocoWeight = chocoWeight;
	}


	public int getChocoPrice() {
		return chocoPrice;
	}


	public void setChocoPrice(int chocoPrice) {
		this.chocoPrice = chocoPrice;
	}

	

	

	

}
